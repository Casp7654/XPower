class ConnectCharacteristic(Characteristic):
    """
    Connect characteristic that handles user read and write.

    """
    BATTERY_LVL_UUID = '0000180d-0000-1000-8000-00805f9b34fb'

    def __init__(self, bus, index, service):
        Characteristic.__init__(
                self, bus, index,
                self.BATTERY_LVL_UUID,
                ['read','write', 'notify'],
                service)
        self.value = []
        self.notifying = False
        self.IP = "0.0.0.0"

    def notify_ip_changed(self):
        if not self.notifying:
            return
        #split ip string to array
        splitIP = self.IP.split('.')

        # 192.168.1.135 will become
        # [0] = 192
        # [1] = 168

        #notify client about changed ip adress
        self.PropertiesChanged(
                GATT_CHRC_IFACE,
                { 'Value': [dbus.Byte(int(splitIP[0])), dbus.Byte(int(splitIP[1])), dbus.Byte(int(splitIP[2])), dbus.Byte(int(splitIP[3]))] }, [])

    # What information is send to client when read is called
    def ReadValue(self, options):
        print('Status read by client')
        return [dbus.Byte(1)]

    # Start process of notifing connected client
    def StartNotify(self):
        if self.notifying:
            print('Already notifying, nothing to do')
            return
        self.notifying = True
        self.notify_ip_changed()

    # message recieved from connected client       
    def WriteValue(self, value, options):
        # format the command send from the client
        command : str = ""

        for v in value:
            command += str(v)

        jObj = json.loads(command)
        id = jObj['ID']

        if(id == 0 or id == "0"):
            
            ssid = jObj['SSID']
            passkey = jObj['PassPhrase']

            # format the json that should be used in the config file,
            # then write the output in console
            p1 = subprocess.Popen(
                ["wpa_passphrase", ssid, passkey], 
                stdout=subprocess.PIPE
            )

            # grab the input from the above and append it to the config file
            # move the old config to "garbage bin" 
            p2 = subprocess.Popen(
                ["sudo", "tee", "a", "/etc/wpa_supplicant/wpa_supplicant.conf", ">", "/dev/null"], 
                stdin=p1.stdout, 
                stdout=subprocess.PIPE
            )

            p1.stdout.close()  # Give p1 a SIGPIPE if p2 dies.

            output,err = p2.communicate()

            # connect to wifi
            os.system("nmcli d wifi connect {} password {} ifname wlan0".format(ssid,passkey))
            
            # grab local ip adress by pinging google
            s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            s.connect(("8.8.8.8", 80))
            self.IP = s.getsockname()[0]
            
            self.notifying = False
            # notify user that ip changed
            self.StartNotify()
     
        self.value = value


    def StopNotify(self):
        if not self.notifying:
            print('Not notifying, nothing to do')
            return

        self.notifying = False


class Advertisement(dbus.service.Object):
    PATH_BASE = '/org/bluez/example/advertisement'

    def __init__(self, bus, index, advertising_type):
        self.path = self.PATH_BASE + str(index)
        self.bus = bus
        self.ad_type = advertising_type
        self.service_uuids = None
        self.manufacturer_data = None
        self.solicit_uuids = None
        self.service_data = None
        self.include_tx_power = None
        dbus.service.Object.__init__(self, bus, self.path)

    def get_properties(self):
        properties = dict()
        properties['Type'] = self.ad_type
        if self.service_uuids is not None:
            properties['ServiceUUIDs'] = dbus.Array(self.service_uuids,
                                                    signature='s')
        if self.solicit_uuids is not None:
            properties['SolicitUUIDs'] = dbus.Array(self.solicit_uuids,
                                                    signature='s')
        if self.manufacturer_data is not None:
            properties['ManufacturerData'] = dbus.Dictionary(
                self.manufacturer_data, signature='qv')
        if self.service_data is not None:
            properties['ServiceData'] = dbus.Dictionary(self.service_data,
                                                        signature='sv')
        if self.include_tx_power is not None:
            properties['IncludeTxPower'] = dbus.Boolean(self.include_tx_power)
        return {LE_ADVERTISEMENT_IFACE: properties}

    def get_path(self):
        return dbus.ObjectPath(self.path)

    def add_service_uuid(self, uuid):
        if not self.service_uuids:
            self.service_uuids = []
        self.service_uuids.append(uuid)

    def add_solicit_uuid(self, uuid):
        if not self.solicit_uuids:
            self.solicit_uuids = []
        self.solicit_uuids.append(uuid)

    def add_manufacturer_data(self, manuf_code, data):
        if not self.manufacturer_data:
            self.manufacturer_data = dbus.Dictionary({}, signature='qv')
        self.manufacturer_data[manuf_code] = dbus.Array(data, signature='y')

    def add_service_data(self, uuid, data):
        if not self.service_data:
            self.service_data = dbus.Dictionary({}, signature='sv')
        self.service_data[uuid] = dbus.Array(data, signature='y')

    @dbus.service.method(DBUS_PROP_IFACE,
                         in_signature='s',
                         out_signature='a{sv}')
    def GetAll(self, interface):
        print ('GetAll')
        if interface != LE_ADVERTISEMENT_IFACE:
            raise InvalidArgsException()
        print ('returning props')
        return self.get_properties()[LE_ADVERTISEMENT_IFACE]

    @dbus.service.method(LE_ADVERTISEMENT_IFACE,
                         in_signature='',
                         out_signature='')
    def Release(self):
        print ('%s: Released!' % self.path)

